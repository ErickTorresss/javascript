// Arrow Function

const sayMayName = (name) => {
    console.log(name)
}

sayMayName('Erick')
/*
    Em resumo, as Arrow Functions são uma forma mais concisa de escrever funções em JavaScript que usam a sintaxe de seta =>. Elas não têm seu próprio contexto de this, o que pode ser útil em certas situações. Além disso, sua sintaxe mais curta pode tornar o código mais fácil de ler e escrever. No entanto, elas não podem ser usadas como construtores, não têm a propriedade arguments e não podem ser usadas como métodos de objetos, pois não possuem seu próprio contexto de this.
 */